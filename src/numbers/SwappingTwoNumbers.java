package numbers;

public class SwappingTwoNumbers {
	
	public static void main(String[] args) {
		
		int x = 10 ; int y = 4;
		
		
		//Logic1
		//Create third variable and assign it to a
		//then assign a to b and b to c
		
		int z=x;//10
		x=y;//4
		y=z;//10
		
		System.out.println("x="+x+"    "+"y="+y);
		
		
		//Logic2
		//using + and minus operators
		//add a and b and assign it to a
		
		int a=10; int b= 5;
		
		a=a+b;//15
		b=a-b;//10
		a=a-b;//5
		
		System.out.println("a="+a+"   "+"b="+b);
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
